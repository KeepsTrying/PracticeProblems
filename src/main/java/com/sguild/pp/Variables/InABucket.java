public class InABucket {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
       
        // You can declare all KINDS of variables
        String walrus;
        double piesEaten;
        float weightOfTeacupPig = 60.5f;
        int grainsOfSand = 40;
        
        //But declaring them just sets up the space for data
        // to use the variable, you have to put data IN it first!
        walrus = "Sir Leroy Jenkins III";
        piesEaten = 42.1;
        
        System.out.println("Meet my pet Walrus, " + walrus);
        System.out.println("He was a bit hungry today, and ate " + piesEaten + " pies!  *cough*fatty*cough*");
        System.out.println("He only ate " + grainsOfSand + " pies yesterday. . . He's almost guaranteed to weigh more than " + weightOfTeacupPig + " pounds now!");
        
        
        
        
    }
    
}
